import { ref, onMounted } from 'vue';
import { message } from 'ant-design-vue';

const showComingSoonMessage = () => {
  message.info('Restez à l\'écoute ! Cette fonctionnalité sera bientôt disponible.');
};

const openEmailContact = () => {
  window.location.href = 'mailto:rafick.shopify@gmail.com?subject=Intéressé par BasketLigue229';
};

// Add Font Awesome CSS
onMounted(() => {
  const link = document.createElement('link');
  link.href = 'https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.4.0/css/all.min.css';
  link.rel = 'stylesheet';
  document.head.appendChild(link);

  // Smooth scroll functionality
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      e.preventDefault();
      const targetId = this.getAttribute('href');
      if (!targetId) return;
      const targetElement = document.querySelector(targetId);
      if (!targetElement) return;
      window.scrollTo({
        top: targetElement.offsetTop,
        behavior: 'smooth'
      });
    });
  });

  // Handle action links
  document.querySelectorAll('a[href="#"]').forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      const text = (e.target as HTMLElement).textContent?.trim().toLowerCase() || '';
      
      if (text.includes('contact') || text.includes('partenaire') || text.includes('rejoindre')) {
        openEmailContact();
      } else {
        showComingSoonMessage();
      }
    });
  });
});
